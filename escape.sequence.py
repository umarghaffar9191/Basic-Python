#-------------------------------------escape sequence------------------------

# escape sequence - escape sequence are control character used to move the cursor and print characters such as ',".\ and so n




#  escape sequence                  meaning 
#   \a                                  bell
#   \b                                  backspace
#   \f                                  formfeed
#   \n                                  new line
#   \r                                  carriage return
#   \t                                  horizontal tab
#   \v                                  vertical tab
#   \newline                            backslash and newline ignored 

#     \\                               backslash
#     \'                              single quote
#   \"                                double quote


#with examples
print("welcome home")
print("welcome \n home")
print("welcome\t home")
print("welcome\"home\"")
print("welcome 'home' ")
print('welcoome\'home\'')
